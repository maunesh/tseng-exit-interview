                <p>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q4a What did you feel were the 
                    greatest two benefits of being enrolled in an online class?</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Every respondent cited flexibility and/or convenience.  
                    Also mentioned were independent learning and cohort members with similar 
                    career paths and goals.</span>
                </p>
                <p style="text-align:center">
                <a name="0.1_graphic0D">
                </a>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">
