                </p>
                <p> </p>
                <p>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q4b The two things you didn’t 
                    like?</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Poor communication and/or lack of engagement were 
                    mentioned by 75% of the respondents.  Other complaints were that 
                    the lecture material is not recorded, more difficult to connect with 
                    classmates, the scheduling of Elluminate, boring lectures and too much 
                    busy work, and difficulty in getting the audio for Elluminate set up.</span>
                </p>
                <p> </p>
                <p style="text-align:center">
                <a name="0.1_graphic0E">
                </a>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">
                    <center>
