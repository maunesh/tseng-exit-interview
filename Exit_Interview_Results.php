                </span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Instructors</span>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt"> – mentioned by 100% of the eight respondents.  
                    Two respondents said “faculty involvement was excellent” or that 
                    s/he held a “positive view of the faculty.”  Although the remainder 
                    of respondents was critical of the engagement aspect on the part of 
                    the faculty, there was recognition that some were more experienced in 
                    the use of the software and that engagement varied from professor to 
                    professor.  Feedback was a problem, with a student specifically 
                    mentioning Professor Toker.  Another student said that faculty 
                    should be required to participate, and yet another indicated that it 
                    appeared that all the professors tried to increase class involvement.</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Coordinator</span>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt"> – this topic was mentioned by 50% of the respondents.  
                    One indicated that the coordinator had been “responsive up to the 
                    last class.”  The others commented that there was poor response time, 
                    if a response was provided at all, and one individual found him to be 
                    “abrasive . . . rude and offensive,” continuing that “I felt . 
                    . .there was no way [a question or concern] would be handled quickly 
                    or politely.”</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Culminating experience</span>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt"> – mentioned by three respondents (37.5%), all 
                    unfavorably.  One student spoke highly of the program except for 
                    the culminating experience.  Others described it as “the worst” 
                    and “terrible.”  Quotes are “very vague course requirements and 
                    grading rubric,” “little to no involvement from an instructor except 
                    . . .notification that I did not pass the final exam,” “I don’t 
                    know still to this day what it was about even though I passed.  
                    No direction, no communication, no one was in charge, no faculty involvement,” 
                    “ . . .no expectations were set.  Didn’t know what to expect.  
                    Didn’t know the culminating experience was a pass/no pass.”</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Miscellaneous</span>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt"> – “More incorporation of international policy 
                    and more classes on specific topics, e.g., healthcare, education, etc.”  
                    One student stated confusion with knowing when the degree would be conferred—in 
                    summer or fall.</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q8 If you were seeking another 
                    course or program that was offered in an online environment, is there 
                    anything you would look for that was not a part of your experience in 
                    this program?</span>
                </p>
                <p style="text-indent:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Two of the respondents (25%) did not feel there was 
                    anything lacking in the program.</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Culminating experience – mentioned by three 
                    respondents (37.5%), of which two would have preferred a thesis.  
                    One assumed it would be a written paper and would investigate the expectations 
                    of the culminating experience prior to taking the class, as the program 
                    was silent on that aspect.  Another comment was that there were 
                    25 books to read.</span>
                </p>
                <p style="text-indent:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Communication – “Administrative communication 
                    was bad.”</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Miscellaneous – “Better technology advancements, 
                    e.g., more video conferencing, more  …group work, providing the 
                    syllabus . . .earlier”</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">“I would look for a program that . . .offered an 
                    in-person component . . .I had to teach myself . . .little interaction 
                    with the professors . . . difficult for students to feel . . .camaraderie 
                    . . .”</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">“Make faculty . . .poll students . . .to find which 
                    night work best for them (not [the] professor”</span>
                </p>
                <p> </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q9 Was enough information about 
                    the online program given to you from the onset when you first made inquiries?</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Of the eight respondents, five (62.5%) responded 
                    positively, two (25%) negatively, and one provided the answer “so-so.”  
                    Notable, however, of the five positive responses, two limited that response 
                    to everything except the culminating experience.  Of those, one 
                    claims to have asked “for months” what the culminating experience 
                    would be with no answer.  And of the two negative responses, one 
                    again criticized the culminating experience, stating that it “was 
                    not representative of the style of work completed for any other course.  
                    No [ne] required a timed test . . .[or] a blind assignment.”  Also 
                    felt the courses were composed of “busy work” and had “very little 
                    recovery (break) time between courses.</span>
                </p>
                <p> </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q10 Do you have any suggestions 
                    for us to improve the educational experience or program in an online 
                    format?</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Culminating experience – Comments from three students:  
                    “ . . .should be improved,” </span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">“ . . .Change the format,” “scrap [it]” in 
                    exchange for having students “write a culminating paper over the course 
                    of a month, or provide specific prompts in advance of the test</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">     . . .[Or] have students 
                    write a policy proposal on a current issue rather than a historical 
                    review of policy.”</span>
                </p>
                <p style="text-align:center">
                <a name="0.1_graphic12">
                </a>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">
                    <center>
                    </center>
                </span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Technology - Comments from two students:  “Find 
                    a service other than Elluminate that would allow for more of an exchange 
                    between everyone,” “Social networking, video conferencing, etc.  
                    It . . .felt outdated compared to what everyone is used to nowadays 
                    “ There was a request for real-time discussions and references to 
                    Google docs for editing group papers.</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Miscellaneous – Scheduling issues, suggesting 
                    polling students for live session times and defining the required participation 
                    and/or attendance at Elluminate.  Also, a comment was made that 
                    administration was the biggest problem.  </span>
                </p>
                <p>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q11 Do you have any last-minute 
                    comments or questions?</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Other than no response or thanks, three students 
                    mentioned that they had learned a lot, that it was a good, unique experience.  
                    Once again, in these final comments, two of the three commented on their 
                    disappointment with the culminating experience.</span>
                </p>
                <p> </p>
                <p>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Summary and Recommendations</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">In summary, the students were mostly satisfied with 
                    their overall learning experience; however there were suggested areas 
                    of improvement regarding “student engagement, faculty involvement 
                    in the classroom, and overall communication with students.”  </span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">The sub-committee believes this effort provided valuable 
                    data and it is recommended that all programs have an exit survey upon 
                    program completion.  In addition, the development of two more surveys 
                    is needed to capture the voice of students who dropped out of the program 
                    or failed to start the program.  </span>
                </p>
