                <p> </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q5  On a scale of 1 to 5, how would 
                    you rate your learning experience of the MPP Program with 5 being the 
                    best?</span>
                </p>
                <p style="text-indent:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">The average of all responses was 3.875, with the 
                    median and the mode both being 4.</span>
                </p>
                <p> </p>
                <p style="text-align:center">
                <a name="0.1_graphic0F">
                </a>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">
