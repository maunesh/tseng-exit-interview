                <h2>Exit Interview Summation</h2>
                <p> </p>
                <p>
                <h3>Overview</h3>
                </p>
                <p style="margin-left:36pt"> The goal of this pilot exit interview was to assess the student’s learning experience for the Master of Public Policy (MPP) degree program at the Tseng College.  This effort also sought to provide data on areas to improve.  The evaluation was conducted over a two-week period ending on October 12, 2012.  There were 14 individuals asked to participate in the evaluation with the option to complete an online survey created in SurveyMonkey or participate in a telephone interview, six participants completed an online survey, while two individuals were interviewed via telephone conversation.  The interview protocols were created for both formats, but in both instances the same questions were asked (see attached.)  The effort produced a 57% participation rate.  All subjects voluntarily participated. </p>
                <p> </p>
                <p>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q1 Was this your first experience 
                    with taking an online course?</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">The majority, 36%, of participants had other online 
                    experiences, but three individuals (21%) stated this was their first 
                    experience taking an online course.</span>
                </p>
                <p style="text-align:center">
                <a name="0.1_graphic0B">
                </a>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">
                    <center>
                    </center>
                </span>
                </p>
                <p> </p>
                <p> </p>
                <p> </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q2 How would you compare this experience 
                    to other educational experiences offered face to face?</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">These responses ran the gamut from stating “there 
                    was more interaction with the professors” from an individual with 
                    a first-time online experience to stating that the instructors “failed 
                    to participate . . . beyond Elluminate.”  </span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">For those who had online experience previously, the 
                    comments suggest that the faculty member was key to their experience.  
                    The forums for the econ class were engaging because the instructor was 
                    active in discussions, more so than in other experiences.  Another 
                    student felt that the program was one directional and that students 
                    lost interaction both with their cohort members and the instructor.</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Students with no previous online experience had varied 
                    experiences.  One commented that responses to his/her questions 
                    to faculty and staff either went unanswered or were very tardy.  
                    Another enjoyed the program but had significant comments regarding displeasure 
                    with the culminating experience and the end result.  Yet another 
                    notation was that the experience was “adequate.”  This group 
                    had a wider spread of positive or negative responses.</span>
                </p>
                <p> </p>
                <p>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q3 Where did you take online courses 
                    before coming to the Tseng College?</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">It appears that this question was misread, since 
                    individuals who stated no previous online experience responded, and 
                    those with experience did not respond.</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">The colleges from which presumably face-to-face classes 
                    had been taken were CSUN, UCLA, Arizona State, Santa Monica and Pierce 
                    Colleges.</span>
                </p>
                <p style="text-align:center">
                <a name="0.1_graphic0C">
