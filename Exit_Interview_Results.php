                <p>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q6 Why did you choose this rating?</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">Fifty percent of the respondents indicated that they 
                    learned a lot.  Two indicated an understanding that the program 
                    was new and attributed some technical difficulties or need for improvement 
                    to that circumstance.  Again, lack of engagement was mentioned 
                    by 37.5%.  Other comments were a repetition of content from class 
                    to class and quicker burnout than with a face-to-face program (again, 
                    lack of interaction as the cause).  Two students (25%) stated unhappiness 
                    with the structure and preparation of the culminating experience.</span>
                </p>
                <p> </p>
                <p> </p>
                <p> </p>
                <p> </p>
