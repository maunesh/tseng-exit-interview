                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Q7 Tell me your impressions of 
                    this online experience overall including tech support, course design, 
                    faculty involvement, etc.</span>
                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Tech support</span>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt"> – mentioned by 50%, 25% stating they had no need, 
                    others regarding it as “excellent” and “so-so.”</span>
                </p>
                <p style="text-align:center">
                <a name="0.1_graphic10">
                </a>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">
