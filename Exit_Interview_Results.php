                </p>
                <p style="margin-left:36pt">
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt;font-weight:bold">Course design</span>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt"> – mentioned by 65.5%, 50% describing it as “great 
                    . . .self-explanatory,” “excellent,” “easy.”  One person said 
                    that it was “not bad.”  “Some professors posted videos of lectures 
                    and during the Elluminate sessions didn’t have any structure to the 
                    live sessions.”</span>
                </p>
                <p style="text-align:center">
                <a name="0.1_graphic11">
                </a>
                <span style="font-family:&#39;Times New Roman&#39;,&#39;Arial&#39;;font-size:12pt">
